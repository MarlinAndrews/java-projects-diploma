public class NewClass {
    public static void main(String[] args) {
        int a = (int) Math.abs(-4.6);
        System.out.println("Absolute value of -4.6: " + a);
        
        int b = (int) Math.ceil(4.6);
        System.out.println("Ceiling of 4.6: " + b);
        
        int c = (int) Math.floor(4.6);
        System.out.println("Floor of 4.6: " + c);
        
        int d = (int) Math.round(4.6);
        System.out.println("Rounded value of 4.6: " + d);
    }
}